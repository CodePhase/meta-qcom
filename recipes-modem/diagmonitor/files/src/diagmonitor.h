/* Requests and responses */
struct diag_frame {
  uint8_t command;
  uint8_t *data[0];
  uint16_t crc;
  uint8_t eof;
};

struct diag_request {
  int data_type;   // USER_SPACE_DATA_TYPE = 32
  int device_type; // MDM = -1 (only if use_mdm returned by the ioctl above is
                   // 1, or field not present)
  char *payload; // Same framing as with the "diag protocol over USB" described
                 // above
};

struct diag_response_buffer {
  int buffer_size;  // Size in bytes of the field below
  char *payload[0]; // Same framing as with the "diag protocol over
                    // USB" described above;
};
struct diag_response_devtype {
  int data_type;   // USER_SPACE_DATA_TYPE = 32
  int device_type; // MDM = -1 (only if use_mdm returned by the ioctl above is
                   // 1, or field not present)

  int nb_buffers; // Number of entries in the field below
  struct diag_response_buffer *response_buffers[0];
};

struct diag_response_notype {
  int data_type;  // USER_SPACE_DATA_TYPE = 32
  int nb_buffers; // Number of entries in the field below
  struct diag_response_buffer *response_buffers[0];
};

