static uint16_t crc16(const uint8_t *data, size_t len)
{
        uint16_t crc = CRC_SEED;

        for(; len != 0; len--, data++)
                crc = crc_ccit16[(crc ^ *data) & 0x00ff] ^ (crc >> 8);

        /* return the 1's complement */
        return ~crc;
}

